<?php

namespace LAP\NotificationsBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Notificationsstatut
 *
 * @ORM\Table(name="notificationsstatut")
 * @ORM\Entity(repositoryClass="LAP\NotificationsBundle\Repository\NotificationsstatutRepository")
 */
class Notificationsstatut
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="status", type="string", length=255)
     */
    private $status;
	
    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set status
     *
     * @param string $status
     *
     * @return Notificationsstatut
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }

    /**
     * Get status
     *
     * @return string
     */
    public function getStatus()
    {
        return $this->status;
    }
}

