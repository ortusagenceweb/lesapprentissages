<?php

namespace LAP\UtilisateurBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Adminroles
 *
 * @ORM\Table(name="adminroles")
 * @ORM\Entity(repositoryClass="LAP\UtilisateurBundle\Repository\AdminrolesRepository")
 */
class Adminroles
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Adminroles
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}

